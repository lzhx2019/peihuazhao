// 获取当前语言，默认为英文
function getCurrentLanguage() {
    const savedLang = localStorage.getItem('preferred-language');
    return savedLang || 'en-US';
}

// 设置页面语言
function setLanguage(lang) {
    if (!translations[lang]) {
        lang = 'zh-CN'; // 默认中文
    }
    
    // 保存语言偏好
    localStorage.setItem('preferred-language', lang);
    
    // 更新HTML lang属性
    document.documentElement.lang = lang;
    
    // 更新所有带data-lang-key属性的元素
    document.querySelectorAll('[data-lang-key]').forEach(element => {
        const key = element.getAttribute('data-lang-key');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // 更新按钮激活状态
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
    
    // 更新页面标题（如果需要）
    updatePageTitle(lang);
}

// 更新页面标题
function updatePageTitle(lang) {
    const currentPage = window.location.pathname;
    let title = '赵培桦 | ';
    
    if (lang === 'en-US') {
        title = 'Peihua Zhao | ';
    }
    
    if (currentPage.includes('about')) {
        title += translations[lang].about;
    } else if (currentPage.includes('photography')) {
        title += translations[lang].photography;
    } else if (currentPage.includes('video')) {
        title += translations[lang].video;
    } else {
        title += (lang === 'en-US') ? 'Personal Website' : '个人网站';
    }
    
    document.title = title;
}

// 初始化语言设置
function initLanguage() {
    const currentLang = getCurrentLanguage();
    setLanguage(currentLang);
    
    // 绑定语言切换按钮事件
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.getAttribute('data-lang');
            setLanguage(lang);
        });
    });
}

// 导出函数供其他文件使用
window.translations = translations;
window.getCurrentLanguage = getCurrentLanguage;
window.setLanguage = setLanguage;
window.initLanguage = initLanguage;

// 页面加载时初始化
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initLanguage);
} else {
    initLanguage();
}